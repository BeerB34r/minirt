#include <libft.h>
#include <ft_printf.h>
#include <fcntl.h>
#include <stdlib.h>

int
	main(
int ac,
char **av
)
{
	int		fd;
	int		i;
	int		j;
	char	*line;
	char	**split;

	if (ac != 2)
		return (ft_dprintf(2, "USAGE: %s [filename.rt]\n", av[0]), 1);
	fd = open(av[1], O_RDONLY);
	if (fd < 0)
		return (ft_dprintf(2, "ERR: unable to allocate file descriptor\n"), 1);
	line = get_next_line(fd);
	i = 0;
	while (line)
	{
		ft_printf("%i: %s", i, line);
		split = ft_split(line, ' ');
		j = -1;
		while (split[++j])
		{
			ft_printf("%i-%i: %s\n", i, j, split[j]);
			free(split[j]);
		}
		i++;
		free(line);
		free(split);
		line = get_next_line(fd);
	}
}
