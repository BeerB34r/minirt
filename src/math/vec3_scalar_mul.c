#include <minirt_declarations.h>

struct s_vec3
	vec3_scalar_mul(
struct s_vec3 a,
double r
)
{
	return ((struct s_vec3){
		.x = a.x * r,
		.y = a.y * r,
		.z = a.z * r
	});
}
