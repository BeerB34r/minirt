#include <minirt_declarations.h>

t_vec3
	vec3_sub(
t_vec3 a,
t_vec3 b
)
{
	return ((t_vec3){
		.x = a.x - b.x,
		.y = a.y - b.y,
		.z = a.z - b.z
	});
}
