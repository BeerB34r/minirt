#include <math.h>
#include <minirt_declarations.h>

double
	sq_e_derivative(
t_line l,
t_vec3 p,
struct s_rt_element_superquadric s
)
{
	return (
		((s.e2
				* (((2 * l.dir.x * pow(p.x / s.a1, (2 / s.e2) - 1))
						/ (s.a1 * s.e2))
					+ ((2 * l.dir.y * pow(p.y / s.a2, (2 / s.e2) - 1))
						/ (s.a2 * s.e2)))
				* pow(
					pow(p.x / s.a1, 2 / s.e2) + pow(p.y / s.a2, 2 / s.e2),
					(s.e2 / s.e1) - 1))
			/ s.e1)
		+ ((2 * l.dir.z * pow(p.z / s.a3, (2 / s.e1) - 1))
			/ (s.a3 * s.e1))
	);
}
