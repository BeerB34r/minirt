#include <math.h>
#include <minirt_math_superquadrics.h>
#include <minirt_math.h>
#include <minirt_declarations.h>

static double
	xuv(
t_uv uv,
struct s_rt_element_superquadric s
)
{
	const double	su = sec(uv.u);
	const double	cv = cos(uv.v);

	if (!su || !cv)
		return (0);
	return (
		(1 / s.a1)
		* pow(su, 2 - s.e1)
		* pow(cv, 2 - s.e2)
	);
}

static double
	yuv(
t_uv uv,
struct s_rt_element_superquadric s
)
{
	const double	su = sec(uv.u);
	const double	sv = sin(uv.u);

	if (!su || ! sv)
		return (0);
	return (
		(1 / s.a2)
		* pow(su, 2 - s.e1)
		* pow(sv, 2 - s.e2)
	);
}

static double
	zuv(
t_uv uv,
struct s_rt_element_superquadric s
)
{
	const double	tu = tan(uv.u);

	if (!tu)
		return (0);
	return (
		(1 / s.a3)
		* -pow(tu, 2 - s.e1)
	);
}

t_norm
	sq_h1_norm(
t_uv uv,
struct s_rt_element_superquadric s
)
{
	const double	x = xuv(uv, s);
	const double	y = yuv(uv, s);
	const double	z = zuv(uv, s);

	return (vec3_normalise((t_vec3){.x = x, .y = y, .z = z}));
}
