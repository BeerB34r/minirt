#include <math.h>
#include <minirt_declarations.h>
#include <minirt_math.h>
#include <minirt_math_superquadrics.h>

t_uv
	sq_h1_xyz_uv(
t_vec3 pw,
struct s_rt_element_superquadric s
)
{
	const t_vec3	p = sq_wp_op(pw, s);
	double			u;
	double			vx;
	double			vy;

	u = atan(pow(p.z / s.a3, 1 / s.e1));
	if (!sec(u))
		return ((t_uv){.u = u, .v = 0});
	vx = acos(pow(p.x / (s.a1 * pow(sec(u), s.e1)), s.e2));
	vy = asin(pow(p.y / (s.a2 * pow(sec(u), s.e1)), s.e2));
	return ((t_uv){.u = u, .v = copysign(vx, vy)});
}
