#include <minirt_declarations.h>

t_vec3
	vec3_cross_product(
t_vec3 a,
t_vec3 b
)
{
	return ((t_vec3){
		.x = a.y * b.z - a.z * b.y,
		.y = a.z * b.x - a.x * b.z,
		.z = a.x * b.y - a.y * b.x,
	});
}
