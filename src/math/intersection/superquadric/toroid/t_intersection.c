#include <math.h>
#include <minirt_declarations.h>
#include <minirt_math_superquadrics.h>
#include <minirt_math.h>
#include <minirt_utils.h>
#include <stdio.h>

#define MAX_NEWTON_ITER 100

// TODO: heuristics
static double
	initial_guess(
t_vec3 pw,
struct s_rt_element_superquadric s
)
{
	(void)pw;
	(void)s;
	return (0);
}

static double
	newton_iter(
t_line lw,
double t,
struct s_rt_element_superquadric s
)
{
	return (t
		- ((sq_t_io(l_t(lw, t), s) - 1)
			/ sq_t_derivative(sq_wl_ol(lw, s), l_t(sq_wl_ol(lw, s), t), s)));
}

static double
	approx(
double n
)
{
	const double	exponent = 10;

	return (round(n * pow(10, exponent)) / pow(10, exponent));
}

int	sq_t_int(t_line lw, struct s_rt_element_superquadric s, double *t, t_uv *uv)
{
	size_t			i;
	double			f_t;

	f_t = initial_guess(lw.origin, s);
	i = -1;
	(void)uv;
	while (++i < MAX_NEWTON_ITER)
	{
		f_t = newton_iter(lw, f_t, s);
		if (approx(sq_t_io(l_t(lw, f_t), s)) == 1)
		{
			*t = f_t;
			return (1);
		}
	}
	return (0);
}
