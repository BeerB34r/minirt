#include <minirt_declarations.h>
#include <minirt_math.h>

struct s_vec3
	vec3_normalise(
struct s_vec3 vector
)
{
	const double	magnitude = vec3_magnitude(vector);

	return ((struct s_vec3){
		.x = vector.x / magnitude,
		.y = vector.y / magnitude,
		.z = vector.z / magnitude
	});
}
