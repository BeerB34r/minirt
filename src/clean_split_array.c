#include <libft.h>
#include <minirt_utils.h>
#include <stdlib.h>

static int
	count_lines(
char ***split_array
)
{
	int	i;

	i = 0;
	while (split_array[i])
		i++;
	return (i);
}

static int
	is_relevant_line(
char **line
)
{
	if (!ft_strncmp(line[0], "#", 1) || !ft_strncmp(line[0], "\n", 1))
		return (0);
	return (1);
}

static int
	count_relevant_lines(
char ***split_array
)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (split_array[i])
		if (is_relevant_line(split_array[i++]))
			count++;
	return (count);
}

int
	clean_split_array(
char ****split_array
)
{
	size_t			i;
	size_t			j;
	char ***const	og = *split_array;
	const int		relevant_lines = count_relevant_lines(og);
	char			***new;

	if (count_lines(og) == relevant_lines)
		return (0);
	new = ft_calloc(relevant_lines + 1, sizeof(char **));
	if (!new)
		return (1);
	i = 0;
	j = 0;
	while (og[i])
	{
		if (!is_relevant_line(og[i]))
		{
			free_array(og[i++]);
			continue ;
		}
		new[j++] = og[i++];
	}
	free(og);
	*split_array = new;
	return (0);
}
