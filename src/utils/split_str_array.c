#include <libft.h>
#include <stdlib.h>

char
	***split_str_array(
const char *const *file_array,
int depth
)
{
	int		i;
	char	***out;

	if (file_array[depth])
		out = split_str_array(file_array, depth + 1);
	else
		return (ft_calloc(depth + 1, sizeof(char **)));
	if (!out)
		return (NULL);
	out[depth] = ft_split(file_array[depth], ' ');
	if (!out[depth])
	{
		while (out[++depth])
		{
			i = 0;
			while (out[depth][i])
				free(out[depth][i++]);
			free(out[depth]);
		}
		free(out);
		return (NULL);
	}
	return (out);
}
