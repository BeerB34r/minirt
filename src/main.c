#include <minirt_parse.h>

/**
 * entrypoint to the program
 *
 * @param ac commandline argument count
 * @param av commandline arguments
 */
int
	main(
int ac,
char **av
)
{
	struct s_rt_scene	scene;
	const int			rval = minirt_parse(ac, av, &scene);

	if (!rval)
		free_scene(&scene);
	return (rval);
}
