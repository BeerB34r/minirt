#include <libft.h>
#include <minirt_parse.h>
#include <minirt_math.h>
#include <minirt_math_superquadrics.h>
#include <stdio.h>

void	render_scene(struct s_rt_scene scene);

/**
 * entrypoint to the program
 *
 * @param ac commandline argument count
 * @param av commandline arguments
 */
int
	main(
int ac,
char **av
)
{
	struct s_rt_scene	scene;
	const int			rval = minirt_parse(ac, av, &scene);

	if (!rval)
	{
		render_scene(scene);
		free_scene(scene);
	}
	printf("closest supqerquadric intersection t = %f\n",
		closest_superquadric_intersection((t_element){
			.type = SUPERQUADRIC,
			.superquadric = (struct s_rt_element_superquadric){
			.a = 1, .b = 1, .c = 1,
			.r = 2, .s = 2, .t = 2
		}
		},
			(struct s_vec3){0, 2, 0},
			vec3_normalise((struct s_vec3){0, 1, 0})
			)
		);
	return (rval);
}
