#include <minirt_declarations.h>

unsigned int
	count_element_type(
enum e_element_type type,
struct s_rt_element *elements,
unsigned int element_count
)
{
	unsigned int	type_count;
	unsigned int	i;

	i = -1;
	type_count = 0;
	while (++i < element_count)
		if (elements[i].type == type)
			type_count++;
	return (type_count);
}
