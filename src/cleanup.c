#include <libft.h>
#include <minirt_parse.h>
#include <stdlib.h>

void
	free_noop(
struct s_rt_element element
)
{
	(void)element;
}

void
	free_stlfile(
struct s_rt_element element
)
{
	free(element.stlfile.triangles);
}

static void (*const	g_cleanup_funcs[10])(struct s_rt_element) = {
	&free_noop, // AMBIENT_LIGHTING
	&free_noop, // CAMERA
	&free_noop, // LIGHT
	&free_noop, // SPHERE
	&free_noop, // PLANE
	&free_noop, // CYLINDER
	&free_noop, // SUPERQUADRIC
	&free_noop, // TRIANGLE
	&free_stlfile, // STLFILE
	&free_noop, // UNDEFINED
};	//	Ordering is mirrored to the enum e_element_type, this is so that it may
	//	be used to directly index into the correct freeing function without an
	//	ifelse forest

void
	free_scene(
struct s_rt_scene scene
)
{
	unsigned int	i;

	i = -1;
	while (++i < scene.element_count)
		g_cleanup_funcs[scene.elements[i].type](scene.elements[i]);
	free(scene.elements);
}
