#include <minirt_mlx.h>
#include <minirt_declarations.h>
#include <MLX42.h>

void
	hit_or_miss_color(
mlx_image_t *img,
t_line angles[VIEWPORT_WIDTH][VIEWPORT_HEIGHT],
struct s_mode_context ctx
)
{
	(void)angles;
	if (ctx.t == ctx.t)
		mlx_put_pixel(img, ctx.x, ctx.y, HIT_OR_MISS_PIXEL_HIT);
	else
		mlx_put_pixel(img, ctx.x, ctx.y, HIT_OR_MISS_PIXEL_MISS);
}
