#include <stdio.h>
#include <minirt_parse.h>
#include <stdlib.h>

#define test(n, case) printf("test " #n ", '" case "': og = %f | oc = %f | [%s]\n", atof(case), rt_atof(case), atof(case) == rt_atof(case) ? "PASS" : "FAIL")

int
	main(
int ac,
char **av
)
{
	(void)ac;
	(void)av;

	test(1, "1");
	test(2, "1.0");
	test(3, "0");
	test(4, "-0");
	test(5, "-0.0");
	test(6, "INF");
	test(7, "-INF");
	test(8, "-");
	test(9, ".");
	test(10, "f");
	test(11, "99999999.99999999");
	test(12, "asd");
	test(13, ".1f");
	test(14, ".0");
	test(15, "-.2");
	test(16, "1.5");

	return (0);
}
